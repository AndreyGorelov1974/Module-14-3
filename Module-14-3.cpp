/*Задание 3. Матрицы
Требуется реализовать небольшую программу для сравнения двух двумерных матриц размером 4 × 4 на предмет их полного равенства.
Если матрицы равны друг другу, необходимо взять одну из матриц и привести её к диагональному виду.
Программа принимает на вход две целочисленные матрицы A и B, которые вводятся с помощью std::cin,
и сравнивает их на предмет полного равенства. Если матрицы равны, то об этом сообщается в стандартном выводе.
Алгоритм должен быть по возможности оптимальным и не проводить лишних операций.
Если матрицы не равны, выводится соответствующее сообщение и выполнение программы прекращается.
Если матрицы равны, то из них выбирается одна и преобразуется в диагональную.
Конвертация в диагональную форму делается путём простейшего зануления всех элементов, лежащих вне главной диагонали.
Напомню, что диагональная матрица — это такая матрица, у которой все элементы вне диагонали равны нулю.
Диагональ матрицы, в свою очередь, — это все элементы, расположенные по диагонали от первого элемента первой строки
до последнего элемента последней строки. Итоговую диагональную матрицу можно расположить на месте прежней матрицы,
не используя дополнительных переменных. Результат преобразования требуется вывести в консоль для проверки.
Ищите именно оптимальное решение, оно довольно простое.

Советы и рекомендации
Чтобы не производить лишних вычислений, стоит при первом же расхождении значений в соответствующих ячейках завершить алгоритм.
Если хотя бы один элемент матриц не совпадает, можно сразу останавливать проверку.
Чтобы в результате получить диагональную матрицу, достаточно пройти по каждому элементу массива и занулить его, если строка не равна столбцу.
Достаточно привести матрицу к диагональному виду. Делать алгоритмическое преобразование не нужно.
Сравнение матриц и приведение матрицы к диагональному виду лучше оформить в виде отдельных функций.*/

#include <iostream>

//размерность матриц
const int n = 4;

//функция сравнения матриц
bool matrix_comparison(int arr1[][n], int arr2[][n]) {
	for (int i = 0; i < n; ++i) {
		for (int j = 0; j < n; ++j) {
			if (arr1[i][j] != arr2[i][j]) { return false; }
		}
	}
	return true;
}

int main()
{
	int A[n][n];
	int B[n][n];

	//инициализация матрицы А
	for (int i = 0; i < n; ++i) {
		for (int j = 0; j < n; ++j) {
			A[i][j] = 5;
		}
	}

	//инициализация матрицы B
	for (int i = 0; i < n; ++i) {
		for (int j = 0; j < n; ++j) {
			B[i][j] = 5;
		}
	}

	//сравниваем матрицы
	if (matrix_comparison(A, B)) {
		std::cout << "The matrices are equal" << std::endl;
		//если равны делаем В диагональной
		for (int i = 0; i < n; ++i) {
			for (int j = i + 1; j < n; ++j) {
				B[i][j] = 0;
				B[j][i] = 0;
			}
		}
		//выводим матрицу В в консоль
		for (int i = 0; i < n; ++i) {
			for (int j = 0; j < n; ++j) {
				std::cout << B[i][j] << " ";
			}
			std::cout << std::endl;
		}
	}
	else {
		std::cout << "The matrices are not equal" << std::endl;
	}
}
